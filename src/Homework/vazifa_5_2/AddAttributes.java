package Homework.vazifa_5_2;

public class AddAttributes {
    public  int add( int a, int b ){
        return a + b;
    }

    public  double add( int a, double b ){
        return a + b;
    }

    public  double add( double a, double b, double c ){
        return a + b + c;
    }

    public  String add( String word1, String word2 ){
        return word1 + word2;
    }

    public  String add( int a, String word ){
        return a + word;
    }
}
